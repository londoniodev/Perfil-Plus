// Map theme slug keywords to SVG icon components
const iconMap: Record<string, { Icon: React.FC<{ className?: string }>; accent: "primary" | "amber" }> = {
    "negocios": { Icon: ChartIcon, accent: "primary" },
    "liderazgo": { Icon: ChartIcon, accent: "primary" },
    "salubridad": { Icon: SanitizerIcon, accent: "amber" },
    "higiene": { Icon: SanitizerIcon, accent: "amber" },
    "culinaria": { Icon: ChefIcon, accent: "primary" },
    "tecnicas": { Icon: KnifeIcon, accent: "primary" },
    "cocina": { Icon: ChefIcon, accent: "primary" },
    "barismo": { Icon: DrinkIcon, accent: "amber" },
    "ventas": { Icon: TrendIcon, accent: "primary" },
    "servicio": { Icon: HandshakeIcon, accent: "amber" },
    "panaderia": { Icon: BreadIcon, accent: "primary" },
    "pasteleria": { Icon: ChefIcon, accent: "amber" },
    "nutricion": { Icon: BreadIcon, accent: "primary" },
    "admin": { Icon: ChartIcon, accent: "primary" },
};

function getIconForTheme(slug: string) {
    for (const [key, val] of Object.entries(iconMap)) {
        if (slug.includes(key)) return val;
    }
    return { Icon: BookIcon, accent: "primary" as const };
}

export function CursosCategorias({ themes }: Props) {
    if (themes.length === 0) return null;

    return (
        <section className="mb-8">
            <div className="flex items-center justify-between px-6 mb-4">
                <h3 className="font-bold text-lg text-foreground">
                    Categorías
                </h3>
                <Link
                    href="/formacion"
                    className="text-xs font-bold text-primary hover:underline"
                >
                    Ver todas
                </Link>
            </div>
            <div className="flex overflow-x-auto gap-3 px-6 pb-4 no-scrollbar">
                {themes.map((theme) => {
                    const { Icon, accent } = getIconForTheme(theme.slug);
                    const bgClass =
                        accent === "primary"
                            ? "bg-primary/10 text-primary group-hover:bg-primary group-hover:text-primary-foreground"
                            : "bg-amber-500/10 text-amber-600 group-hover:bg-amber-500 group-hover:text-white";

                    return (
                        <Link
                            key={theme.id}
                            href={`/formacion/${theme.slug}`}
                            className="flex-shrink-0 flex flex-col items-center gap-2 group cursor-pointer"
                        >
                            <div
                                className={`w-16 h-16 rounded-2xl ${bgClass} flex items-center justify-center transition-colors duration-200`}
                            >
                                <Icon className="w-7 h-7" />
                            </div>
                            <div className="text-center">
                                <span className="text-xs font-semibold text-muted-foreground group-hover:text-foreground transition-colors block max-w-[72px] truncate">
                                    {theme.title.split(" ")[0]}
                                </span>
                                <span className="text-[10px] text-muted-foreground/60">
                                    {theme._count?.courses ?? 0} curso
                                    {(theme._count?.courses ?? 0) !== 1
                                        ? "s"
                                        : ""}
                                </span>
                            </div>
                        </Link>
                    );
                })}
            </div>
        </section>
    );
}
